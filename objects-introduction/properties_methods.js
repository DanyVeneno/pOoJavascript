// Objects has properties and methods

// Properties are data
const person = {
  name: "John", // property
  lastname: "Carter",
};

// methods are just properties with function as value
// So, when a function is inside an object it could be called a 'method'

const person2 = {
  name: "Ryan", // property
  lastname: "Ray", // property
  age: 30, //property
  sayHello: function () { // method
    return "Hello I'am Ryan"
  }
}
